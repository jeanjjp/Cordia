package model;

public class Cirurgia {

	private int idCirurgia;
	private int idCliente;
	private String nomeCirigia;
	
	
	public int getIdCirurgia() {
		return idCirurgia;
	}
	public void setIdCirurgia(int idCirurgia) {
		this.idCirurgia = idCirurgia;
	}
	public String getNomeCirigia() {
		return nomeCirigia;
	}
	public void setNomeCirigia(String nomeCirigia) {
		this.nomeCirigia = nomeCirigia;
	}
	public int getIdCliente() {
		return idCliente;
	}
	public void setIdCliente(int idCliente) {
		this.idCliente = idCliente;
	}
	
	
	
	
}
